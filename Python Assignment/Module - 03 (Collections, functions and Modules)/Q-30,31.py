# Q-30 : convert a list of tuples into a dictionary.

list_1 = [('a', 1), ('b', 2), ('c', 3)]
dict_1= dict(list_1)
print("Dictionary from list of tuples:",dict_1)

# Q-31 : How will you create a dictionary using tuples.

tuple_1 = (('a', 1), ('b', 2), ('c', 3))
dict_1 = dict(tuple_1)
print("Dictionary using tuples:", dict_1)
